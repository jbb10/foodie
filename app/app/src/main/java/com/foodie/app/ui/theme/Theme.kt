/**
 * Foodie app theme composable.
 *
 * Applies Material Design 3 theming with support for:
 * - Light and dark modes (AC-1, AC-2, AC-6)
 * - System theme following (default behavior)
 * - Dynamic color on Android 12+ (Material You)
 * - Manual theme override via darkTheme parameter (AC-7)
 *
 * @param darkTheme True to use dark theme, false for light. Defaults to system preference.
 * @param dynamicColor True to use Android 12+ dynamic color (Material You), false for static palette.
 * @param content The composable content to apply the theme to.
 *
 * Usage in MainActivity:
 * ```kotlin
 * val isDarkMode = when (themePreference) {
 *     "light" -> false
 *     "dark" -> true
 *     else -> isSystemInDarkTheme()
 * }
 * FoodieTheme(darkTheme = isDarkMode) { ... }
 * ```
 *
 * Story 5.4: Dark Mode Support
 */
@Composable
fun FoodieTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}
